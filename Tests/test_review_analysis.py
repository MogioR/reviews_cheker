    def test_check_end_of_sentence(self):
        # True end of sentence
        assert self.a.check_end_of_sentence('Sentence.') is True
        assert self.a.check_end_of_sentence('Sentence..') is True
        assert self.a.check_end_of_sentence('Sentence...') is True
        assert self.a.check_end_of_sentence('Sentence....') is True
        assert self.a.check_end_of_sentence('Sentence!') is True
        assert self.a.check_end_of_sentence('Sentence!!') is True
        assert self.a.check_end_of_sentence('Sentence!!!') is True
        assert self.a.check_end_of_sentence('Sentence!!!!') is True

        # False end of sentence
        assert self.a.check_end_of_sentence('Sentence') is False
        assert self.a.check_end_of_sentence('Sentence!.') is False
        assert self.a.check_end_of_sentence('Sentence.!') is False
        assert self.a.check_end_of_sentence('Sentence.....') is False
        assert self.a.check_end_of_sentence('Sentence!.!!') is False
        assert self.a.check_end_of_sentence('Sentence!..') is False

