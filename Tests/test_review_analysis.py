    def test_check_end_of_sentence(self):
        # True end of sentence
        self.assertTrue(self.a.check_end_of_sentence('Sentence.'))
        self.assertTrue(self.a.check_end_of_sentence('Sentence..'))
        self.assertTrue(self.a.check_end_of_sentence('Sentence...'))
        self.assertTrue(self.a.check_end_of_sentence('Sentence....'))
        self.assertTrue(self.a.check_end_of_sentence('Sentence!'))
        self.assertTrue(self.a.check_end_of_sentence('Sentence!!'))
        self.assertTrue(self.a.check_end_of_sentence('Sentence!!!'))
        self.assertTrue(self.a.check_end_of_sentence('Sentence!!!!'))

        # False end of sentence
        self.assertFalse(self.a.check_end_of_sentence('Sentence'))
        self.assertFalse(self.a.check_end_of_sentence('Sentence!.'))
        self.assertFalse(self.a.check_end_of_sentence('Sentence.!'))
        self.assertFalse(self.a.check_end_of_sentence('Sentence.....'))
        self.assertFalse(self.a.check_end_of_sentence('Sentence!.!!'))
        self.assertFalse(self.a.check_end_of_sentence('Sentence!..'))

